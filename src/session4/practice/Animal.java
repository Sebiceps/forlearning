package session4.practice;

public class Animal {

    public String name;
    public int age;
    private double weight;
    private boolean isWild;
    private String sex;
    private String diet;
    private String colour;
    private String animalType;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}